namespace FlutterSharp.UI
{
    /// The style in which to draw a text decoration
    public enum TextDecorationStyle
    {
        /// Draw a solid line
        Solid,

        /// Draw two lines
        Double,

        /// Draw a dotted line
        Dotted,

        /// Draw a dashed line
        Dashed,

        /// Draw a sinusoidal line
        Wavy
    }
}